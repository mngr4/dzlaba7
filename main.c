#include <stdio.h>
#include <locale.h>

/*
 * Программа определяет возрастную категорию ребёнка по введённому возрасту
 * в месяцах. Категории распределены следующим образом:
 * - новорожденный: 0–2 месяца
 * - младенец: 3–12 месяцев
 * - раннее детство: 13–36 месяцев
 * - дошкольник: 37–84 месяца
 * - школьник: 85–156 месяцев
 * - подросток: 157–204 месяца
 * При возрасте старше 17 лет (более 204 месяцев) выдаётся соответствующее
 * сообщение.
 */

int main(void) {
    /* Устанавливаем локаль для корректного отображения русских букв */
    setlocale(LC_ALL, "");

    int months;
    printf("Введите возраст ребёнка в месяцах: ");
    if (scanf("%d", &months) != 1) {
        /* Обработка некорректного ввода */
        printf("Ошибка ввода. Пожалуйста, введите целое число.\n");
        return 1;
    }

    /* Определяем возрастную категорию по значениям месяцев */
    if (months <= 2) {
        printf("Возрастная категория: новорожденный\n");
    } else if (months <= 12) {
        printf("Возрастная категория: младенец\n");
    } else if (months <= 36) {
        printf("Возрастная категория: раннее детство\n");
    } else if (months <= 84) {
        printf("Возрастная категория: дошкольник\n");
    } else if (months <= 156) {
        printf("Возрастная категория: школьник\n");
    } else if (months <= 204) {
        printf("Возрастная категория: подросток\n");
    } else {
        printf("Возрастная категория: старше 17 лет\n");
    }

    return 0;
}